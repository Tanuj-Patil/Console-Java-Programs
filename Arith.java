class Arith{
	public static void main(String args[]){
		int i = 5;
		int j = 10;
		int sum = i+j;
		int diff = i-j;
		int mult = i*j;
		float div = i/j;
		int mode = i%j;
	
		System.out.println("The addition of i and j is: " +sum);
		System.out.println("The subtraction of i and j is: " +diff);
		System.out.println("The multiplication of i and j is: " +mult);
		System.out.println("The division of i and j is: " +div); 
		System.out.println("The modulo of i by j is: " +mode);
		System.out.printf("Hello");
		}
}